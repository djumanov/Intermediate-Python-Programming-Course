# Lists: ordered, mutable, allows duplicated elements
mylist = ["banana", "cherry", "apple"]
print(mylist)

# empty list
mylist2 = list()
print(mylist2)

mylist3 = []
print(mylist3)

# list with mixed data types
mylist4 = [5, True, "apple"]
print(mylist4)

# list of integers
my_list = [1, 2, 3]

# nested list
my_list = ["mouse", [8, 4, 6], ['a']]
