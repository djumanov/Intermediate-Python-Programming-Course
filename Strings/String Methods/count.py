# Count occurrences of 'Big' in the string
S = 'Big, Bigger, Biggest'
x = S.count('Big')
print(x)
# Prints 3

# Count occurrences of 'Big' from 5th character
S = 'Big, Bigger, Biggest'
x = S.count('Big',5)
print(x)
# Prints 2

# Count occurrences of 'Big' between 5th to 13th character
S = 'Big, Bigger, Biggest'
x = S.count('Big',5,13)
print(x)
# Prints 1

